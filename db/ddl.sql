CREATE OR REPLACE TRIGGER trg_audit_app_status
AFTER UPDATE OF status ON JOB_APPLICATION
FOR EACH ROW
BEGIN
    INSERT INTO AUDIT_LOG (db_user, table_name, operation, old_value, new_value)
    VALUES (
        SYS_CONTEXT('USERENV', 'SESSION_USER'), -- inregistreaza cine a facut modificarea 
        'JOB_APPLICATION',
        'UPDATE',
        :OLD.status,
        :NEW.status
    );
END;
/


