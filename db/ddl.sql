CREATE OR REPLACE PACKAGE security_pkg IS
    FUNCTION get_key RETURN RAW;
    FUNCTION encrypt_salary(p_amount NUMBER) RETURN RAW;
    FUNCTION decrypt_salary(p_encrypted_data RAW) RETURN NUMBER;
    
    PROCEDURE app_login(p_user VARCHAR2, p_pass VARCHAR2, p_role OUT VARCHAR2);
    PROCEDURE set_session_context(p_user VARCHAR2, p_role VARCHAR2);
END security_pkg;
/

CREATE OR REPLACE PACKAGE BODY security_pkg IS

    FUNCTION get_key RETURN RAW IS 
        v_key RAW(32); v_count NUMBER; PRAGMA AUTONOMOUS_TRANSACTION;
    BEGIN
        SELECT count(*) INTO v_count FROM KEY_STORE WHERE key_name = 'SALARY_KEY';
        IF v_count = 0 THEN
            v_key := DBMS_CRYPTO.RANDOMBYTES(32);
            INSERT INTO KEY_STORE (key_name, encryption_key) VALUES ('SALARY_KEY', v_key);
            COMMIT;
        ELSE SELECT encryption_key INTO v_key FROM KEY_STORE WHERE key_name = 'SALARY_KEY'; END IF;
        RETURN v_key;
    END;

    FUNCTION encrypt_salary(p_amount NUMBER) RETURN RAW IS
        v_key RAW(32); v_src RAW(2000);
    BEGIN
        IF p_amount IS NULL THEN RETURN NULL; END IF;
        v_key := get_key();
        v_src := UTL_I18N.STRING_TO_RAW(TO_CHAR(p_amount), 'AL32UTF8');
        RETURN DBMS_CRYPTO.ENCRYPT(src=>v_src, typ=>DBMS_CRYPTO.ENCRYPT_AES256+DBMS_CRYPTO.CHAIN_CBC+DBMS_CRYPTO.PAD_PKCS5, key=>v_key);
    END;

    FUNCTION decrypt_salary(p_encrypted_data RAW) RETURN NUMBER IS
        v_key RAW(32); v_decrypted_raw RAW(2000);
    BEGIN
        IF p_encrypted_data IS NULL THEN RETURN 0; END IF;
        v_key := get_key();
        v_decrypted_raw := DBMS_CRYPTO.DECRYPT(src=>p_encrypted_data, typ=>DBMS_CRYPTO.ENCRYPT_AES256+DBMS_CRYPTO.CHAIN_CBC+DBMS_CRYPTO.PAD_PKCS5, key=>v_key);
        RETURN TO_NUMBER(UTL_I18N.RAW_TO_CHAR(v_decrypted_raw, 'AL32UTF8'));
    EXCEPTION WHEN OTHERS THEN RETURN -1;
    END;

    PROCEDURE app_login(p_user VARCHAR2, p_pass VARCHAR2, p_role OUT VARCHAR2) IS
        v_stored_hash VARCHAR2(64);
        v_role VARCHAR2(20);
        v_locked NUMBER;
    BEGIN

        SELECT password_hash, role, is_locked INTO v_stored_hash, v_role, v_locked
        FROM APP_USER WHERE username = p_user;
        
        IF v_locked = 1 THEN
            RAISE_APPLICATION_ERROR(-20001, 'Account Locked due to Security Policy.');
        END IF;

        IF v_stored_hash = p_pass THEN

            DBMS_SESSION.SET_CONTEXT('recruitment_ctx', 'ROLE', v_role);
            DBMS_SESSION.SET_CONTEXT('recruitment_ctx', 'USER', p_user);

            UPDATE APP_USER SET failed_attempts = 0, last_login = SYSTIMESTAMP WHERE username = p_user;
            COMMIT;
            
            p_role := v_role;
        ELSE

            UPDATE APP_USER SET failed_attempts = failed_attempts + 1 WHERE username = p_user;
            COMMIT;
            RAISE_APPLICATION_ERROR(-20002, 'Invalid Credentials');
        END IF;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            RAISE_APPLICATION_ERROR(-20003, 'User not found');
    END;
    PROCEDURE set_session_context(p_user VARCHAR2, p_role VARCHAR2) IS
    BEGIN
        DBMS_SESSION.SET_CONTEXT('recruitment_ctx', 'ROLE', p_role);
        DBMS_SESSION.SET_CONTEXT('recruitment_ctx', 'USER', p_user);
    END set_session_context;
END security_pkg;
/

