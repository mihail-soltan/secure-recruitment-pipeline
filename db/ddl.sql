CREATE OR REPLACE VIEW smart_candidate_view AS
SELECT 
    c.candidate_id,
    c.first_name,
    c.last_name,
    c.email,
    CASE 
       
        WHEN SYS_CONTEXT('recruitment_ctx', 'ROLE') = 'HR' THEN c.phone
        
        ELSE '***-***-' || SUBSTR(c.phone, -4)
    END AS phone,
    ja.status,
    ja.app_id
FROM CANDIDATE c
JOIN JOB_APPLICATION ja ON c.candidate_id = ja.candidate_id;



